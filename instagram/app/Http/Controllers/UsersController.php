    public function update(Request $request, $id)
    {
        $user = User::findOrFail($id);
        
        $request->validate([
            'name' => 'required',
            'username' => 'required',
        ]);       
       
        $user->name = $request->input('name');
        
        $user->username = $request->input('username');        
        
        $user->save();
   
        return redirect("/profile/{$user->id}")->with('success','updated successfully.');
    
        
